    public void excluirElementoLista(int num_id) {
        for (int i = 0; i < listaImagens.size(); i++) {
            if (num_id == listaImagens.get(i).getId()) {
                listaImagens.remove(i);
                counter--;
                Caixa1.removeItemAt(i + 1);
                Caixa2.removeItemAt(i + 1);
            }
        }
    }

