    private void botaoAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAbrirActionPerformed
        JFileChooser arquivo = new JFileChooser();
        int retorno = arquivo.showOpenDialog(null);
        BMP bmp = null;
        if (retorno == arquivo.APPROVE_OPTION) {
            String nomeImg;
            try {
                File f = arquivo.getSelectedFile();
                RandomAccessFile rf = new RandomAccessFile(f, "r");
                byte[] b = new byte[(int) rf.length()];
                rf.read(b);
                rf.close();
                bmp = new BMP(b);
            } catch (IOException ex) {
                Logger.getLogger(CellBusters.class.getName()).log(Level.SEVERE, null, ex);
            }

            nomeImg = arquivo.getSelectedFile().getName();
            JanelaImgs jal = new JanelaImgs(bmp, this, counter);
            int ct = 0;
            for (int i = 0; i < listaImagens.size(); i++) {
                if (listaImagens.get(i).getName().equals(nomeImg)) {
                    ct++;
                }
            }
            if (ct > 0) {
                boolean flag = false;
                String nomeAux = nomeImg;
                nomeImg = nomeImg + " (" + ct + ")";
                for (int i = 0; i < listaImagens.size(); i++) {
                    if (listaImagens.get(i).getTitle().equals(nomeImg)) {
                        flag = true;
                    }
                }
                if (flag) {
                    nomeAux = nomeAux + " (" + (ct + 1) + ")";
                    jal.setTitle(nomeAux);
                    Caixa1.addItem(nomeAux);
                    Caixa2.addItem(nomeAux);
                } else {
                    jal.setTitle(nomeImg);
                    Caixa1.addItem(nomeImg);
                    Caixa2.addItem(nomeImg);
                }
            } else {
                jal.setTitle(nomeImg);
                Caixa1.addItem(nomeImg);
                Caixa2.addItem(nomeImg);
            }
            jal.setName(nomeImg);
            jal.getImagem().setImagem(bmp);
            jal.getImagem().reset();
            jal.getImagem().update();
            jal.setVisible(true);
            listaImagens.add(jal);
            counter++;
        }
    }//GEN-LAST:event_botaoAbrirActionPerformed

    private void botaoAplicaOpeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAplicaOpeActionPerformed
        if (Caixa1.getSelectedIndex() != 0) {
            if (Caixa2.getSelectedIndex() != 0) {
                if (buttonGroup1.getSelection() != null) {
                    BMP b1, b2, aux;
                    String nome1, nome2;
                    int index1 = Caixa1.getSelectedIndex() - 1;
                    int index2 = Caixa2.getSelectedIndex() - 1;
                    b1 = listaImagens.get(index1).getImagem().getImagem();
                    b2 = listaImagens.get(index2).getImagem().getImagem();
                    nome1 = listaImagens.get(index1).getTitle();
                    nome2 = listaImagens.get(index2).getTitle();
                    
                    /* A abordagem nas próximas 2 linhas de código realiza a 
                     * cópia do conteúdo da variável "b1", ao invés de obter  
                     * sua referência. Isto é feito para evitar 
                     * que as alterações feitas na variável "aux" sejam 
                     * espelhadas para a variável "b1", que por sua vez é uma
                     * referência para uma imagem contida na lista. */
                    byte[] copia = b1.getTudo().clone();
                    aux = new BMP(copia); 
                    //
                    
                    String titulo;
                    if (opAND.isSelected()) {
                        aux.getMp().opAND(b2.getMp());
                        titulo = nome1 + " AND " + nome2;
                    } else {
                        if (opOR.isSelected()) {
                            aux.getMp().opOR(b2.getMp());
                            titulo = nome1 + " OR " + nome2;
                        } else {
                            aux.getMp().opXOR(b2.getMp());
                            titulo = nome1 + " XOR " + nome2;
                        }
                    }
                    JanelaImgs jal = new JanelaImgs(aux, this, counter);
                    jal.setTitle(titulo);
                    jal.getImagem().setImagem(aux);
                    jal.getImagem().reset();
                    jal.getImagem().update();
                    jal.setVisible(true);
                    listaImagens.add(jal);
                    Caixa1.addItem(titulo);
                    Caixa2.addItem(titulo);
                    counter++;
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Selecione a operação", null, JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Selecione a imagem 2", null, JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione a imagem 1", null, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botaoAplicaOpeActionPerformed

